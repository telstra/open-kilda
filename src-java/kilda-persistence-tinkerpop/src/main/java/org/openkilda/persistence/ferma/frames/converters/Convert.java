package org.openkilda.persistence.ferma.frames.converters;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * An annotation is for getter and setters to define conversion of the property value.
 */
@Retention(RetentionPolicy.RUNTIME)
@Target(ElementType.METHOD)
public @interface Convert {
    /**
     * The type of {@link AttributeConverter} implementation to apply on the property.
     */
    Class<? extends AttributeConverter> value();
}

package org.openkilda.server42.stats.swagger;

import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import springfox.documentation.builders.ApiInfoBuilder;
import springfox.documentation.builders.RequestHandlerSelectors;
import springfox.documentation.service.ApiInfo;
import springfox.documentation.spi.DocumentationType;
import springfox.documentation.spring.web.plugins.Docket;
import springfox.documentation.swagger2.annotations.EnableSwagger2;


@EnableSwagger2
@Configuration
public class SwaggerApiDocumentationConfig {

    private ApiInfo apiInfo() {
        return new ApiInfoBuilder().title("Server42 stats REST CRUD")
                .description(
                        "HTTP control on server42-stats")
                .termsOfServiceUrl("").version("0.0.1-SNAPSHOT").build();
    }

    @Bean
    public Docket configureControllerPackageAndConvertors() {
        return new Docket(DocumentationType.SWAGGER_2).select()
                .apis(RequestHandlerSelectors.basePackage("org.openkilda.java.server42.stats")).build()
                .apiInfo(apiInfo());
    }
}

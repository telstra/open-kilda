package org.openkilda.persistence.tests.neo4j;

import static org.junit.Assert.assertFalse;

import org.junit.Test;
import org.openjdk.jmh.results.RunResult;
import org.openjdk.jmh.runner.Runner;
import org.openjdk.jmh.runner.RunnerException;
import org.openjdk.jmh.runner.options.Options;
import org.openjdk.jmh.runner.options.OptionsBuilder;

import java.io.IOException;
import java.util.Collection;

public class Neo4jWithFermaPersistenceBenchmarkTest {
    @Test
    public void runBenchmark() throws RunnerException, IOException {
        Options opt = new OptionsBuilder()
                .include(Neo4jWithFermaPersistenceBenchmark.class.getSimpleName())
                .build();
        Collection<RunResult> runResults = new Runner(opt).run();
        assertFalse(runResults.isEmpty());
    }
}
